package com.tochange.yang.view;

public class Position
{
    public int x;

    public int y;

    public Position(int x, int y)
    {
        super();
        this.x = x;
        this.y = y;
    }

    public Position()
    {
        super();
    }

    public String toString()
    {
        return "Position [x=" + x + ", y=" + y + "]";
    }

}
